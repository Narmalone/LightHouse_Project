                    EditorUtility.SetDirty(asset);
                }
                else
                {
                    Debug.LogWarning($"[ApplySettings] Nom '{sanitized}' non trouvé dans l'enum AmbianceAudioName");
                }
            }
        }

        public List<AmbianceAudio> FindAmbiancesAssets()
        {
            string[] ambianceGuids = GetGuids("AmbianceAudio");

            List<AmbianceAudio> audiosAssets = new();
            foreach (string ambianceGuid in ambianceGuids)
            {
                string path = AssetDatabase.GUIDToAssetPath(ambianceGuid);
                AmbianceAudio asset = AssetDatabase.LoadAssetAtPath<AmbianceAudio>(path);
                audiosAssets.Add(asset);
            }
            return audiosAssets;
        }

