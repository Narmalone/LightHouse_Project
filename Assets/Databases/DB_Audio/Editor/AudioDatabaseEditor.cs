        private string[] GetGuids(string assetName)
        {
            return AssetDatabase.FindAssets($"t:{assetName}");
        }

        private void GenerateEnumFile(string enumName, string filePath, List<string> entries)
        {
            if (entries.Count == 0)
            {
                Debug.LogWarning($"No entries found for {enumName}");
                return;
            }

            System.Text.StringBuilder sb = new System.Text.StringBuilder();
            sb.AppendLine("/// <auto-generated/>");
            sb.AppendLine("namespace LightHouse.Audio.AutoGenerated");
            sb.AppendLine("{");
            sb.AppendLine($"    public enum {enumName}");
            sb.AppendLine("    {");
            foreach (string entry in entries)
            {
                sb.AppendLine($"        {entry},");
            }
            sb.AppendLine("    }");
            sb.AppendLine("}");

            string newContent = sb.ToString();
            // Vérifie si le fichier existe et si le contenu est identique
            if (System.IO.File.Exists(filePath))
            {
                string existingContent = System.IO.File.ReadAllText(filePath);
                if (existingContent == newContent)
                {
                    Debug.Log($"{enumName} ŕ jour — aucune modification nécessaire.");
                    return; // Rien ŕ faire
                }
            }

            if (System.IO.File.Exists(filePath))
            {
                if (!EditorUtility.DisplayDialog("Fichier déjŕ existant",
                    $"Le fichier '{enumName}.cs' existe déjŕ. Voulez-vous le remplacer ?", "Oui", "Non"))
                {
                    return;
                }
            }

            // write the file
            System.IO.Directory.CreateDirectory(System.IO.Path.GetDirectoryName(filePath));
            System.IO.File.WriteAllText(filePath, sb.ToString());
            AssetDatabase.Refresh();

            Debug.Log($"{enumName} généré avec succčs !");
        }

        private string SanitizeName(string rawName)
        {
            string clean = rawName.Replace(" ", "_")
                                  .Replace("-", "_")
                                  .Replace(".", "_");

            // Remove all non numerical characters
            clean = System.Text.RegularExpressions.Regex.Replace(clean, @"[^a-zA-Z0-9_]", "");

            return clean;
        }


