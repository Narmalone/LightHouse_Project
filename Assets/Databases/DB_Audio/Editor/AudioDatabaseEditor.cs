        public void GenerateAmbiancesEnum()
        {
            string enumName = "AmbianceAudioName";
            string filePath = BasePath + enumName + ".cs";

            // Trouver tous les assets AmbianceAudio
            string[] guids = AssetDatabase.FindAssets("t:AmbianceAudio");

            List<string> names = new List<string>();

            foreach (var guid in guids)
            {
                string path = AssetDatabase.GUIDToAssetPath(guid);
                AmbianceAudio asset = AssetDatabase.LoadAssetAtPath<AmbianceAudio>(path);
                if (asset != null && !string.IsNullOrEmpty(asset.displayName))
                {
                    string cleanName = SanitizeName(asset.displayName);
                    if (!names.Contains(cleanName))
                        names.Add(cleanName);
                }
            }

            GenerateEnumFile(enumName, filePath, names);
        }

